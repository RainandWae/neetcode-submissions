# Hash Set/defaultdict abusing
# see the sudoku board as 0.0, 0.1, ... 2.1, 2.2

# Uses three defaultdict(set)s to track numbers
# seen in each row, column, and 3×3 box, immediately 
# returning False if a duplicate is found.
class Solution:
    def isValidSudoku(self, board: List[List[str]]) -> bool:
        cols = defaultdict(set)
        rows = defaultdict(set)
        squares = defaultdict(set)

        for r in range(9):
            for c in range(9):
                if board[r][c] == ".":
                    continue
                if ( board[r][c] in rows[r]
                    or board[r][c] in cols[c]
                    or board[r][c] in squares[(r // 3, c // 3)]):
                    return False

                cols[c].add(board[r][c])
                rows[r].add(board[r][c])
                squares[(r // 3, c // 3)].add(board[r][c])

        return True